#include "ft_ls.h"

void		ft_stradd_nb(char *s, long nb)
{
	int		i;
	int		a;
	char	*main;

	i = -1;
	a = 1;
	main = "0123456789ABCDEF";
	if (nb == 0)
		s[0] = 0 + '0';
	else
	{
		if (nb < 0)
		{
			s[++i] = '-';
			nb *= -1;
		}
		while (a <= nb / 10)
			a *= 10;
		while (nb)
		{
			s[++i] = main[(nb / a) % 10];
			nb %= a;
			a /= 10;
		}
	}
}
